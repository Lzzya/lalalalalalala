class Vehicle(object):

    def __init__(self, id, name, weight, volume, driving_range, charge_time, unit_cost, day_cost):
        self.id = id
        self.name = name
        self.weight = weight
        self.volume = volume
        self.driving_range = driving_range
        self.charge_time = charge_time
        self.unit_cost = unit_cost
        self.day_cost = day_cost
